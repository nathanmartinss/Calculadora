public class Calculadora {

    /**
     *
     * @param a, primeiro valor inteiro a ser recebido e utilizado na soma, subtração, multiplicação ou divisão.
     * @param b, segundo valor inteiro a ser recebido e utilizado na soma, subtração, multiplicação ou divisão.
     * @return retorna o resultado da soma, subtração, multiplicação ou divisão.
     */

    // Faz a soma de dois números inteiros.
    public int soma(int a, int b) {
        return a + b;
    }

    // Faz a subtração de dois números inteiros.
    public int subtracao(int a, int b) {
        return a - b;
    }

    // Faz a divisão de dois números inteiros.
    public int divisao(int a, int b) {
        return a / b;
    }

    // Faz a multiplicação de dois números inteiros.
    public int multiplicacao(int a, int b) {
        return a * b;
    }

}
